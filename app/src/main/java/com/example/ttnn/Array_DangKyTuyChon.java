package com.example.ttnn;

public class Array_DangKyTuyChon {
    String MaKhoa, TenKhoa, TenBS;

    public Array_DangKyTuyChon(String maKhoa, String tenKhoa, String tenBS) {
        MaKhoa = maKhoa;
        TenKhoa = tenKhoa;
        TenBS = tenBS;
    }

    public String getMaKhoa() {
        return MaKhoa;
    }

    public void setMaKhoa(String maKhoa) {
        MaKhoa = maKhoa;
    }

    public String getTenKhoa() {
        return TenKhoa;
    }

    public void setTenKhoa(String tenKhoa) {
        TenKhoa = tenKhoa;
    }

    public String getTenBS() {
        return TenBS;
    }

    public void setTenBS(String tenBS) {
        TenBS = tenBS;
    }
}
