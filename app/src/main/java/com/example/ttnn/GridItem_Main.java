package com.example.ttnn;

public class GridItem_Main {
    int Hinh;
    String Chu;

    public GridItem_Main(int hinh, String chu) {
        Hinh = hinh;
        Chu = chu;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }

    public String getChu() {
        return Chu;
    }

    public void setChu(String chu) {
        Chu = chu;
    }
}
